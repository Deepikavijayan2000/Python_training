### This is a basic program which reads 2 numbers from user and execute basic arithmetic operations
### "input() " is the keyword we use to take a input from user

num1 = int(input("Entre the first number"))
num2 = int(input("Entre the second number"))
add = num1 + num2
sub = num1 - num2
mul = num1 * num2
divi = num1/ num2
print(f"\n {add} \n {sub} \n {mul}\n {divi}")
